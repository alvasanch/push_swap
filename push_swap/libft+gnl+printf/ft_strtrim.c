#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	char	*ptr;
	int		start;
	int		end;

	start = 0;
	if (!s1[start])
		return (ft_strdup(""));
	end = ft_strlen(s1);
	while (ft_strchr(set, s1[start]))
		start++;
	while (ft_strchr(set, s1[end]))
		end--;
	ptr = ft_substr(s1, start, (end - start) + 1);
	return (ptr);
}
